    @Override
    public JTable getTable() {
        return tblRequest;
    }

    @Override
    public Object[] populateRow(AdViewsOrder element) {
        return new Object[]{
            element,
            element.getCompany().getName(),
            element.getQuantity(),
            element.getInformation(),
            element.getStatus()
        };
    }

    @Override
    public void populateTable() {
        for(AdCompanyEnterprise companyEnterprise : account.getOrg().getEnterprise().getNetwork().getAdCompanyCatalog().getEnterprises()){
            Stream<AdViewsOrder> stream = companyEnterprise.getAdViewsOrderCatalog().getAdViewsOrders()
                .stream().filter(e -> e.getStatus().equals(AdViewsOrder.Status.Submitted));
            populateTable(stream);
        }
    }

    @Override
    public String getTitle() {
        return "Manage Advertisement Order Request";
    }
