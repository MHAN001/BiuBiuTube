    private void btnApproveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApproveActionPerformed
        AdViewsOrder order = getSelected();
        if(order == null){
            return;
        }
        try{

            order.approve();
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Cannot approve because of some system reasons");
            populateTable();
        }
        JOptionPane.showMessageDialog(this, "Approved");
        populateTable();
    }//GEN-LAST:event_btnApproveActionPerformed

    private void btnRejectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRejectActionPerformed
        AdViewsOrder order = getSelected();
        if(order == null){
            return;
        }
        try{
            order.reject();
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Cannot reject because of some system reasons");
            populateTable();
        }
        JOptionPane.showMessageDialog(this, "Rejected");
        populateTable();
    }//GEN-LAST:event_btnRejectActionPerformed


